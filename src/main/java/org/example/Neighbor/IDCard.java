package org.example.Neighbor;

public class IDCard {
    String name;
    String hair;
    String eyes;
    String mouth;
    String skin;
    String id_number;

    public IDCard(String name, String hair, String eyes, String mouth, String skin, String id_number) {
        this.name = name;
        this.hair = hair;
        this.eyes = eyes;
        this.mouth = mouth;
        this.skin = skin;
        this.id_number = id_number;
    }
}